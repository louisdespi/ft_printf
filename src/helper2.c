#include "ft_printf.h"

char	cut_sign(t_field *field)
{
	char	*t;
	char	sign;

	t = field->value;
	sign = 0;
	while (*t)
	{
		if (*t == '+' || *t == '-')
		{
			sign = *t;
			*t = ' ';
		}
		t++;
	}
	return (sign);
}
